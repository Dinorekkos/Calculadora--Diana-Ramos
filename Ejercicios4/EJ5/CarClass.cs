
public class CarClass
{
   
   private string colorCar;
   private float weightCar;
   private float heightCar;
   private int areaCar;


    public CarClass()
    {
    }

    public CarClass(float aWeight)
    {
        weightCar = aWeight;
    }

    public CarClass(int aAreaCar)
    {
        areaCar= aAreaCar;
    }

    public CarClass(string aColorCar, float aWeightCar, float aHeightCar, int aAreaCar)
    {
        colorCar = aColorCar;
        weightCar = aWeightCar;
        heightCar = aHeightCar;
        areaCar = aAreaCar;
    }







}
